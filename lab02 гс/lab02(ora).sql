CREATE OR REPLACE PROCEDURE AddBalance(p_UserID IN NUMBER, p_Amount IN NUMBER) IS
BEGIN
    UPDATE Users SET Balance = Balance + p_Amount WHERE UserID = p_UserID;
    COMMIT;
END;
/

