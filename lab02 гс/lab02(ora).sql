CREATE TABLE Roles (
    RoleID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    RoleName VARCHAR2(50) NOT NULL UNIQUE
);

CREATE TABLE VehicleModels (
    ModelID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Brand VARCHAR2(100),
    ModelName VARCHAR2(100) NOT NULL,
    MaxSpeedKMH NUMBER,
    WeightLimitKG NUMBER,
    VehicleType VARCHAR2(20) CHECK (VehicleType IN ('самокат', 'велосипед'))
);

CREATE TABLE Tariffs (
    TariffID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Name VARCHAR2(50) NOT NULL,
    UnlockPrice NUMBER(10, 2) NOT NULL,
    MinutePrice NUMBER(10, 2) NOT NULL,
    IsActive NUMBER(1) DEFAULT 1 CHECK (IsActive IN (0, 1))
);

CREATE TABLE Users (
    UserID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FullName VARCHAR2(255) NOT NULL,
    Email VARCHAR2(100) NOT NULL UNIQUE,
    Phone VARCHAR2(20) NOT NULL UNIQUE,
    Balance NUMBER(10, 2) DEFAULT 0.00 CHECK (Balance >= 0),
    RoleID NUMBER NOT NULL,
    CreatedAt TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_User_Role FOREIGN KEY (RoleID) REFERENCES Roles(RoleID)
);

CREATE TABLE Vehicles (
    VehicleID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    QRCode VARCHAR2(50) NOT NULL UNIQUE,
    ModelID NUMBER NOT NULL,
    TariffID NUMBER NOT NULL,
    BatteryLevel NUMBER DEFAULT 100 CHECK (BatteryLevel BETWEEN 0 AND 100),
    Status VARCHAR2(20) DEFAULT 'доступно' 
        CHECK (Status IN ('доступно', 'бронь', 'ремонт', 'зарядка', 'сломано')),
    Lat NUMBER(9, 6),
    Lon NUMBER(9, 6),
    LastServiceDate TIMESTAMP,
    CONSTRAINT FK_Vehicle_Model FOREIGN KEY (ModelID) REFERENCES VehicleModels(ModelID),
    CONSTRAINT FK_Vehicle_Tariff FOREIGN KEY (TariffID) REFERENCES Tariffs(TariffID)
);

CREATE TABLE Rentals (
    RentalID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    UserID NUMBER NOT NULL,
    VehicleID NUMBER NOT NULL,
    StartTime TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    EndTime TIMESTAMP,
    FixedMinutePrice NUMBER(10, 2) NOT NULL,
    TotalCost NUMBER(10, 2),
    RentalStatus VARCHAR2(20) DEFAULT 'активно' 
        CHECK (RentalStatus IN ('активно', 'завершено', 'отменено')),
    CONSTRAINT FK_Rental_User FOREIGN KEY (UserID) REFERENCES Users(UserID),
    CONSTRAINT FK_Rental_Vehicle FOREIGN KEY (VehicleID) REFERENCES Vehicles(VehicleID)
);



CREATE INDEX IX_Users_Email ON Users(Email);

CREATE VIEW AvailableVehicles AS
SELECT v.VehicleID, v.QRCode, m.Brand, m.ModelName, t.MinutePrice, v.BatteryLevel, v.Lat, v.Lon
FROM Vehicles v
JOIN VehicleModels m ON v.ModelID = m.ModelID
JOIN Tariffs t ON v.TariffID = t.TariffID
WHERE v.Status = 'доступно';

