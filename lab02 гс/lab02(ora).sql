CREATE OR REPLACE FUNCTION GetUserRentalCount(p_UserID IN NUMBER) RETURN NUMBER IS
    v_Count NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_Count FROM Rentals WHERE UserID = p_UserID;
    RETURN v_Count;
END;
/

-- 2
CREATE OR REPLACE FUNCTION EstimateCost(p_TariffID IN NUMBER, p_Minutes IN NUMBER) RETURN NUMBER IS
    v_Unlock NUMBER;
    v_MinPrice NUMBER;
BEGIN
    SELECT UnlockPrice, MinutePrice INTO v_Unlock, v_MinPrice FROM Tariffs WHERE TariffID = p_TariffID;
    RETURN v_Unlock + (v_MinPrice * p_Minutes);
END;
/

