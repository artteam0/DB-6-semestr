CREATE OR REPLACE FUNCTION BatteryLabel(p_Level IN NUMBER) RETURN VARCHAR2 IS
BEGIN
    IF p_Level < 20 THEN RETURN 'Низкий';
    ELSIF p_Level < 70 THEN RETURN 'Средний';
    ELSE RETURN 'Высокий';
    END IF;
END;
/

-- 4
CREATE OR REPLACE FUNCTION CanUserRent(p_UserID IN NUMBER) RETURN NUMBER IS
    v_Bal NUMBER;
BEGIN
    SELECT Balance INTO v_Bal FROM Users WHERE UserID = p_UserID;
    IF v_Bal > 0 THEN RETURN 1; ELSE RETURN 0; END IF;
END;
/

-- 5
CREATE OR REPLACE FUNCTION GetActiveRentalID(p_UserID IN NUMBER) RETURN NUMBER IS
    v_ID NUMBER;
BEGIN
    SELECT RentalID INTO v_ID FROM Rentals 
    WHERE UserID = p_UserID AND RentalStatus = 'активно' AND ROWNUM = 1;
    RETURN v_ID;
EXCEPTION WHEN NO_DATA_FOUND THEN RETURN NULL;
END;
/

-- 1
CREATE OR REPLACE PROCEDURE StartRental(p_UserID IN NUMBER, p_VehicleID IN NUMBER) IS
    v_Price NUMBER;
BEGIN
    SELECT t.MinutePrice INTO v_Price FROM Vehicles v JOIN Tariffs t ON v.TariffID = t.TariffID WHERE v.VehicleID = p_VehicleID;
    UPDATE Vehicles SET Status = 'бронь' WHERE VehicleID = p_VehicleID;
    INSERT INTO Rentals (UserID, VehicleID, FixedMinutePrice, RentalStatus)
    VALUES (p_UserID, p_VehicleID, v_Price, 'активно');
    COMMIT;
END;
/

-- 2
CREATE OR REPLACE PROCEDURE EndRental(
    p_RentalID IN NUMBER, 
    p_Lat IN NUMBER, 
    p_Lon IN NUMBER
) IS
    v_minutes NUMBER;
    v_start TIMESTAMP;
    v_price NUMBER;
BEGIN
    -- Получаем данные аренды
    SELECT StartTime, FixedMinutePrice 
    INTO v_start, v_price
    FROM Rentals 
    WHERE RentalID = p_RentalID;
    
    -- РАСЧЁТ МИНУТ: (текущее время - время старта) в днях * 24 * 60
    -- Используем CAST в DATE для простой арифметики
    v_minutes := ROUND(
        (CAST(CURRENT_TIMESTAMP AS DATE) - CAST(v_start AS DATE)) * 24 * 60
    );
    
    -- Если прошло меньше минуты - всё равно считаем как 1 минуту
    IF v_minutes < 1 THEN
        v_minutes := 1;
    END IF;
    
    -- Обновляем аренду с правильной стоимостью
    UPDATE Rentals 
    SET EndTime = CURRENT_TIMESTAMP, 
        RentalStatus = 'завершено',
        TotalCost = v_minutes * v_price
    WHERE RentalID = p_RentalID;

    -- Возвращаем транспорт на парковку
    UPDATE Vehicles 
    SET Status = 'доступно', 
        Lat = p_Lat, 
        Lon = p_Lon 
    WHERE VehicleID = (SELECT VehicleID FROM Rentals WHERE RentalID = p_RentalID);
    
    COMMIT;
    
    -- Для отладки
    DBMS_OUTPUT.PUT_LINE('Аренда ' || p_RentalID || 
                        ', минут: ' || v_minutes || 
                        ', цена минуты: ' || v_price || 
                        ', итого: ' || (v_minutes * v_price));
END;
/

