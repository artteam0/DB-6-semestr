CREATE OR REPLACE PROCEDURE RegisterUser(p_Name IN VARCHAR2, p_Email IN VARCHAR2, p_Phone IN VARCHAR2, p_RoleID IN NUMBER) IS
BEGIN
    INSERT INTO Users (FullName, Email, Phone, RoleID) VALUES (p_Name, p_Email, p_Phone, p_RoleID);
    COMMIT;
END;
/

