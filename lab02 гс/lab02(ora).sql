CREATE OR REPLACE PROCEDURE SetVehicleMaintenance(p_VehicleID IN NUMBER) IS
BEGIN
    UPDATE Vehicles SET Status = 'ремонт' WHERE VehicleID = p_VehicleID;
    COMMIT;
END;
